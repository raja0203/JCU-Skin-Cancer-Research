    <h3>Our History</h3>
		<p>The JCU Skin Cancer Research Unit was established in Townsville, North Queensland, 

Australia in 1990 thanks to the tireless devotion of the Late Emeritus Professor Bob MacLennan. Professor Bob MacLennan was a Professor at the Queensland Institute of 

Medical Research and a representative on the Queensland Cancer Fund's Medical and Scientific Committee in the 1980s-1990s. Bob was an internationally respected skin 

cancer epidemiologist, who recognised the need for skin cancer research to be carried out in North Queensland, where the incidence of skin cancer is among the highest 

in the world. <p>
		<p>The group was also lucky enough to have the mentorship of the Late Emeritus Professor Rick Speare for more than two decades. Rick 

was a public health physician whose vast knowledge, passion for science and enthusiasm for finding practical, "real-world" solutions to tackle public health issues 

was a positive influence for the JCU Skin Cancer Research Unit from the early 1990s. The selflessness of our mentors and the significant contribution they both made 

to public health research and training still continues to inspire us.  <p>

