<p style="font-size:17px;"><i>If so, devoting as little as <span class="color-red">3-5 minutes once a month</span> to counting hat-wearing practices at your local school is enough to help us improve sun-safety in Queensland schools.</i></p>
<p style="font-size:17px;"><i>If you want to collect hat-wearing data more often (e.g. weekly) or at several different schools in the area, you are most welcome, but even small contributions are valued and appreciated.</i></p>

<h3>What is involved?</h3>
<ul style="font-size:17px;">
<li>Provide your contact details by visiting our volunteer sign-up page <a href="register.php">here</a></li>
<li>Obtain a current Blue Card for Working with Children, if you don't already have one. It's FREE!
<a href="downloads.php">apply or renew your card here</a> Past/present teachers may be exempt; </li>
<li>Provide us with a copy of your Blue Card details for validation.</li>
<li><a href="files/Observations%20of%20hat%20wearing%20manual%2018april17.pdf">Download and read our brief training manual</a> to learn how to collect reliable research data on the hat-wearing practices of Queensland schoolchildren and their adult role-models and about the importance of data confidentiality;</li>
<li>Practice your new hat-counting research skills by completing a quick on-line quiz;</li>
<li>After accomplishing these steps, you will be able to download our smartphone app so that you can use your phone to record, store and send hat research data to us. That way, each time you are at school when the children are outside, and have 3 minutes to spare, you will be all set to record data for the project. </li>
</ul>

<h3>What happens to the data I collect? </h3>
<p style="font-size:17px;">The data you and other volunteer researchers collect will be managed in a central database. Data will be collated periodically by season and time of day (before school/ play breaks/ outdoor sport/ school dismissal observations) so that results can be reported back to the schools themselves. That way, school management can see how they are performing relative to other schools in the area.</p>

